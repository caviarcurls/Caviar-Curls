const params = new URLSearchParams(window.location.search);

const productKey = params.get("product");

const product = products[productKey];

if(product){

document.getElementById("product-title").innerText =
product.title;

if(product.density){

document.getElementById("product-type").innerText =
`${product.type} • ${product.density}`;

}else{

document.getElementById("product-type").innerText =
product.type;

}

document.getElementById("product-description").innerText =
product.description;

document.getElementById("product-image").src =
product.image;

const laceGroup =
document.getElementById("lace-group");

const laceSelect =
document.getElementById("lace-select");

const lengthSelect =
document.getElementById("length-select");

laceGroup.style.display = "block";

laceSelect.innerHTML = "";
lengthSelect.innerHTML = "";

product.lace.forEach(lace=>{

const option =
document.createElement("option");

option.value = lace;

option.textContent = lace;

laceSelect.appendChild(option);

});

function populateLengths(){

lengthSelect.innerHTML = "";

const selectedLace =
laceSelect.value;

Object.keys(
product.prices[selectedLace]
).forEach(length=>{

const option =
document.createElement("option");

option.value = length;

option.textContent = `${length}"`;

lengthSelect.appendChild(option);

});

updatePrice();

}

function updatePrice(){

const selectedLace =
laceSelect.value;

const selectedLength =
lengthSelect.value;

const finalPrice =
product.prices[selectedLace][selectedLength];

document.getElementById("product-price").innerText =
`£${finalPrice}`;

}

laceSelect.addEventListener(
"change",
populateLengths
);

lengthSelect.addEventListener(
"change",
updatePrice
);

populateLengths();

}
